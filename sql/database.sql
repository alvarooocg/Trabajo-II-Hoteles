CREATE TABLE HOTELES
(
    ID            VARCHAR(9),
    NOMBRE        VARCHAR(100),
    CIUDAD        VARCHAR(50),
    ESTRELLAS     NUMERIC(1),
    DESCRIPCION   VARCHAR(400),

    CONSTRAINT "PK_HOTELES"                PRIMARY KEY (ID),
    CONSTRAINT "NN_HOTELES.NOMBRE"         CHECK (NOMBRE IS NOT NULL),
    CONSTRAINT "NN_HOTELES.CIUDAD"         CHECK (CIUDAD IS NOT NULL),
    CONSTRAINT "NN_HOTELES.ESTRELLAS"      CHECK (ESTRELLAS IS NOT NULL),
    CONSTRAINT "CH_HOTELES.ESTRELLAS"      CHECK (ESTRELLAS BETWEEN 1 AND 5)
);

CREATE TABLE HABITACIONES
(
    ID           VARCHAR(9),
    ID_HOTEL     VARCHAR(9),
    NUMERO       VARCHAR(10),
    TIPO         VARCHAR(50),
    PRECIO       NUMERIC(10, 2),
    DISPONIBLE   BOOLEAN,

    CONSTRAINT "PK_HABITACIONES"              PRIMARY KEY (ID),
    CONSTRAINT "FK_HABITACIONES_HOTELES"      FOREIGN KEY (ID_HOTEL) REFERENCES HOTELES(ID),
    CONSTRAINT "NN_HABITACIONES.ID_HOTEL"     CHECK (ID_HOTEL IS NOT NULL),
    CONSTRAINT "NN_HABITACIONES.NUMERO"       CHECK (NUMERO IS NOT NULL),
    CONSTRAINT "NN_HABITACIONES.TIPO"         CHECK (TIPO IS NOT NULL),
    CONSTRAINT "NN_HABITACIONES.PRECIO"       CHECK (PRECIO IS NOT NULL),
    CONSTRAINT "CH_HABITACIONES.PRECIO"       CHECK (PRECIO > 0)
);

CREATE TABLE CLIENTES
(
    ID          VARCHAR(9),
    NOMBRE      VARCHAR(100),
    EMAIL       VARCHAR(100),
    TELEFONO    VARCHAR(15),

    CONSTRAINT "PK_CLIENTES"               PRIMARY KEY (ID),
    CONSTRAINT "NN_CLIENTES.NOMBRE"        CHECK (NOMBRE IS NOT NULL),
    CONSTRAINT "NN_CLIENTES.EMAIL"         CHECK (EMAIL IS NOT NULL),
    CONSTRAINT "NN_CLIENTES.TELEFONO"      CHECK (TELEFONO IS NOT NULL)
);

CREATE TABLE RESERVAS
(
    ID             VARCHAR(9),
    ID_CLIENTE     VARCHAR(9),
    ID_HABITACION  VARCHAR(9),
    FECHA_ENTRADA  DATE,
    FECHA_SALIDA   DATE,

    CONSTRAINT "PK_RESERVAS"                 PRIMARY KEY (ID),
    CONSTRAINT "FK_RESERVAS_CLIENTES"        FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID),
    CONSTRAINT "FK_RESERVAS_HABITACIONES"    FOREIGN KEY (ID_HABITACION) REFERENCES HABITACIONES(ID),
    CONSTRAINT "NN_RESERVAS.ID_CLIENTE"      CHECK (ID_CLIENTE IS NOT NULL),
    CONSTRAINT "NN_RESERVAS.ID_HABITACION"   CHECK (ID_HABITACION IS NOT NULL),
    CONSTRAINT "NN_RESERVAS.FECHA_ENTRADA"   CHECK (FECHA_ENTRADA IS NOT NULL),
    CONSTRAINT "NN_RESERVAS.FECHA_SALIDA"    CHECK (FECHA_SALIDA IS NOT NULL),
    CONSTRAINT "CH_RESERVAS.FECHA"           CHECK (FECHA_SALIDA > FECHA_ENTRADA)
);

